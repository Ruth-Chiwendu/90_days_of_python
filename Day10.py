#!/usr/bin/env python3

print ("Introductio to Libraries")
print ("TASK: Write a Python script that calculates the square root of a number using the math library")

import math

# Take user input and change it to integer
number = int(input("Enter a number: "))


# Calculate the square root
sqrt_result = math.sqrt(number)

# Print the result
print(f"The square root of {number} is {sqrt_result}")


